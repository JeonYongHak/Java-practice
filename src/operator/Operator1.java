package src.operator;

public class Operator1 {
    public static void main(String[] args) {
        // 변수 초기화
        int a = 5;
        int b= 2;

        // 덧셈

        int sum = a + b;
        System.out.println("a + b = " + sum); // 출력 a + b = 3

        // 뺄셈
        int diff = a - b;
        System.out.println("a - b = " + diff); // 출력 a -b =3;
        // 곱셈
        int multi = a *b;
        System.out.println(multi);

        //나눗셈
        double div= a /b ;
        System.out.println(div);
        //나머지
        int mod = 5 % 2;
        System.out.println(mod); // 몫을 제외하고 나머지가 출력이 됨



    }
}
