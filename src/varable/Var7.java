package src.varable;

public class Var7 {
    public static void main(String[] args) {
        int a =10; // 정수
        long b = 12L;
        double c = 10.5; // 실수
        boolean d = true; // 불리언, true or false값 입력 가능
        char e = 'A'; // 문자 1개만 들어감
        String f ="Hello Java"; // 문자열, 문자열을 다루기 위한 특별한 타입

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);

    }
}
