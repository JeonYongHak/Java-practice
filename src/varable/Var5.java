package src.varable;

public class Var5 {
    public static void main(String[] args) {
        int a ; // 변수 선언
        a =10; //
        System.out.println(a);

        int b =25; // 변수 선언과 초기화를 한 번에 진행함!
        System.out.println(b);

        int c = 3, d = 6; // 여러 변수 선언과 초기화를 한번에
        System.out.println(c);
        System.out.println(d);
    }
}
